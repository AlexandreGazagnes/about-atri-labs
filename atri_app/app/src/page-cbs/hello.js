import { useCallback } from "react";
import { callbackFactory } from "../utils/callbackFactory";
export function useDiv6Cb() {
	const onClick = useCallback(callbackFactory("Div6", "hello", "/hello", "onClick", 
			{
  "handlers": [],
  "actions": [
    {
      "type": "do_nothing"
    }
  ]
}), [])
	return { onClick }
}
